# Ejemplos de Listas (append)

# Creamos esta Lista inicial
compra = ["pan", "leche", "huevos"]
# Imprimimos el contenido inicial
print(f"Inicio: {compra}") # Salida: ['pan', 'leche', 'huevos']

# Añadir un elemento al final
compra.append("fruta") 

# Imprimimos el contenido después de usar append
print(f"Después de append(): {compra}") # Salida: ['pan', 'leche', 'huevos', 'fruta']